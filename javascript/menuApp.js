const categorie5Drop = () => {
  const banner = document.querySelector('.banner5');
  const bannerSvg = document.querySelector('.bannerSvg5');
  const bannerH3 = document.querySelector('.bannerH35');
  const bannerPlus = document.querySelector('.bannerPlus5');
  const categorieUl = document.querySelector('.categorieUl5');


  bannerPlus.addEventListener('click', () => {
    banner.classList.toggle('bannerNav-active');
    bannerSvg.classList.toggle('bannerSvgNav-active');
    bannerH3.classList.toggle('bannerH3Nav-active');
    bannerPlus.classList.toggle('bannerPlusNav-active');
    categorieUl.classList.toggle('nav-deactive');

  });
}

categorie5Drop();

